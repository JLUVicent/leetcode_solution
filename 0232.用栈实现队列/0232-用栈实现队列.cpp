class MyQueue {
public:
    stack<int> stIn;
    stack<int> stOut;
    MyQueue() {

    }
    
    void push(int x) {
        stIn.push(x);   
    }
    
    int pop() {
        //当stOut为空时候导入数据
        if( stOut.empty()){
            while(!stIn.empty()){
                stOut.push(stIn.top());
                stIn.pop();
            }
        }
        int result = stOut.top();
        stOut.pop();
        return result;
    }
    
    int peek() {
        int res = this->pop();
        stOut.push(res);    //pop弹出了栈顶元素，再添加回去
        return res;
    }
    
    bool empty() {
        return stIn.empty()&&stOut.empty();
    }
};

