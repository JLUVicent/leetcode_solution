class Solution {
public:
    int monotoneIncreasingDigits(int n) {
        string s = to_string(n);
        int flag = s.size();   //定义标志位
        for( int i =s.size()-1 ; i > 0; i--){
            if( s[i] < s[i-1]){
                flag = i;
                //前一个数要-1
                s[i-1]--;
            }
        }
        for( int i = flag; i < s.size(); i++){
            s[i] = '9';
        }
        return stoi(s);

    }
};